func SaveImage(c *fiber.Ctx) error {
	newImage := new(ImagenStruct)
	err := c.BodyParser(newImage)
	if err != nil {
		c.Status(400).JSON(&fiber.Map{
			"success": false,
			"message": err,
			"data":    nil,
		})
		return err
	}
	return nil
}

